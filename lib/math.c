//-------------------------------------------------------------------------
//定点整数开平方
//-------------------------------------------------------------------------
u32 sqrtfix(u64 d, size_t n)
{
	u64 t;
	u32 q = 0;

	while (n--)
	{
		t = (q << 1) + (1 << n);
		if ((d >> n) >= t)
		{
			d -= (t << n);
			q += (1 << n);
		}
	}
	
	return q;
}





